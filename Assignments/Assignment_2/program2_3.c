#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name   :   Display
//  Description     :   It is used to print 'Hello' number accepted from user is less than 10, otherwise prints 'Demo'.
//  Input           :   Int
//  Output          :   void      
//  Author          :   Shraddha Dhananjay Mutange
//  Date            :   23/10/2025
// 
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    if(iNo < 10)
    {
        printf("Hello");
    }
    else
    {
        printf("Demo");
    }

} // End of Display

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;                     // To accept the user input

    printf("Enter number :\n");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
}

