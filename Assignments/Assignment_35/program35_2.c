///////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : CheckSmall
//  Description     : It accepts a string from user and counts the number of small characters
//  Input           : Char
//  Output          : Int
//  Author          : Shraddha Dhananjay Mutange
//  Date            : 28/11/2025
//
