#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : CountWhitespaces
//  Description     : It accepts a string from the user and returns number of white spaces
//  Input           : Char
//  Output          : Void
//  Author          : Shraddha Dhananjay Mutange
//  Date            : 28/11/2025
//
///////////////////////////////////////////////////////////////////////////////////////

int CountWhitespaces(char str[])
{
    int iCount = 0;

    while (*str != '\0')
    {
        if (*str == ' ' || *str == '\t')
        {
            iCount++;
        }

        str++;
    }

    return iCount;

} // End of CountWhitespaces

///////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = {'\0'};
    int iRet = 0;

    printf("Enter a string : \n");
    scanf("%[^'\n']s", Arr);

    iRet = CountWhitespaces(Arr);

    printf("Total number of whitespaces : %d\n", iRet);

    return 0;
}

