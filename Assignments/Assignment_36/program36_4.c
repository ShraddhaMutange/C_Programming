void DisplayDigit(char str[])
{
    while (*str != '\0')
    {
        if (*str >= '0' && *str <= '9')
        {
            printf("%c", *str);
        }

        str++;
    }
    printf("\n");
} // End of Toggle

///////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = {'\0'};

    printf("Enter a string : \n");
    scanf("%[^'\n']s", Arr);

    DisplayDigit(Arr);

    return 0;
}

