void struprX(char str[])
{
    while (*str != '\0')
    {
        if (*str >= 'a' && *str <= 'z')
        {
            *str = *str - 32;
        }

        str++;
    }
} // End of struprX

///////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = {'\0'};

    printf("Enter a string : \n");
    scanf("%[^'\n']s", Arr);

    struprX(Arr);
    printf("Updated string : %s\n", Arr);

    return 0;
}

