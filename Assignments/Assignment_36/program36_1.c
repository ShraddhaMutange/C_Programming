void strlwrX(char str[])
{
    while (*str != '\0')
    {
        if (*str >= 'A' && *str <= 'Z')
        {
            *str = *str + 32;
        }

        str++;
    }
} // End of strlwrX

///////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = {'\0'};

    printf("Enter a string : \n");
    scanf("%[^'\n']s", Arr);

    strlwrX(Arr);
    printf("Updated string : %s\n", Arr);

    return 0;
}

