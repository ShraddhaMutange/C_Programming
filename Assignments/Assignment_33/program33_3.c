#include <stdio.h>
#include <stdbool.h>

///////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : CheckCapital
//  Description     : It accepts a character from user and checks whether it is capital or not
//  Input           : Char
//  Output          : Bool
//  Author          : Shraddha Dhananjay Mutange
//  Date            : 27/11/2025
//
///////////////////////////////////////////////////////////////////////////////////////

bool CheckCapital(char ch)
{
    bool bFlag = false;

    if ((ch >= '0') && (ch <= '9'))
    {
        bFlag = true;
    }

    return bFlag;
}

///////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char ch = '\0';
    bool bRet = false;

    printf("Enter a character : \n");
    scanf("%c", &ch);

    bRet = CheckCapital(ch);

    if (bRet == true)
    {
        printf("%c is digit\n", ch);
    }
    else
    {
        printf("%c is not digit\n", ch);
    }

    return 0;
}

