#include <stdio.h>
#include <stdbool.h>

///////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : CheckSmall
//  Description     : It accepts a character from user and checks whether it is small or not
//  Input           : Char
//  Output          : Bool
//  Author          : Shraddha Dhananjay Mutange
//  Date            : 27/11/2025
//
///////////////////////////////////////////////////////////////////////////////////////

bool CheckSmall(char ch)
{
    bool bFlag = false;

    if ((ch >= 'a') && (ch <= 'z'))
    {
        bFlag = true;
    }

    return bFlag;
}

///////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char ch = '\0';
    bool bRet = false;

    printf("Enter a character : \n");
    scanf("%c", &ch);

    bRet = CheckSmall(ch);

    if (bRet == true)
    {
        printf("%c is small\n", ch);
    }
    else
    {
        printf("%c is not small\n", ch);
    }

    return 0;
}

