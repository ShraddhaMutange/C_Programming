#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : Display
//  Description     : It accepts a character from the user and displays its ASCII value in decimal, octal and hexadecimal format
//  Input           : Char
//  Output          : Void
//  Author          : Shraddha Dhananjay Mutange
//  Date            : 28/11/2025
//
///////////////////////////////////////////////////////////////////////////////////////

void Display(char ch)
{
    printf("Decimal\t%d\n", ch);
    printf("Octal\t%o\n", ch);
    printf("Hex\t%X\n", ch);

} // End of Display

///////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
///////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char ch = '\0';

    printf("Enter a character : \n");
    scanf("%c", &ch);

    Display(ch);

    return 0;
}

