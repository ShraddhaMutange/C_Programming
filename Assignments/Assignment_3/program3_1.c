#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : PrintEven
//  Description     : It accepts one number from user and print that number of even numbers on screen
//  Input           : Int
//  Output          : void
//  Author          : Shraddha Dhananjay Mutange
//  Date            : 23/10/2025
// 
////////////////////////////////////////////////////////////////////////////////////////////////////////

void PrintEven(int iNo)
{
    int iCnt = 0;

    if(iNo <= 0)
    {
        return;
    }

    for(iCnt = 2; iCnt <= (iNo * 2); iCnt++)
    {
        if((iCnt % 2) == 0)
        {
            printf("%d\t", iCnt);
        }
    }
} // End of PrintEven

////////////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number : ");
    scanf("%d", &iValue);

    PrintEven(iValue);

    return 0;
}

