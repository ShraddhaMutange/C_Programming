#include <stdio.h>

/////////////////////////////////////////////////////////////////////////
// 
//  Function Name   : Display
//  Description     : It is used to print 5 to 1 numbers on screen
//  Input           : None
//  Output          : Void
//  Author          : Shraddha Dhananjay Mutange
//  Date            : 18/10/2025
// 
/////////////////////////////////////////////////////////////////////////
void Display()
{
    int iCnt = 0;

    iCnt = 5;
    while(iCnt != 0)
    {
        printf("%d\n", iCnt);
        iCnt--;
    }
} // End of Display

/////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}

