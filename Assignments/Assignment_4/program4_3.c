#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name   : NonFact
//  Description     : It accepts number from user and displays all its non factors
//  Input           : Int
//  Output          : Void
//  Author          : Shraddha Dhananjay Mutange
//  Date            : 24/10/2025
// 
////////////////////////////////////////////////////////////////////////////////////////////////

void NonFact(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        if(((iNo % iCnt) != 0))
        {
            printf("%d\t", iCnt);
        }
    }
} // End of NonFact

////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number : ");
    scanf("%d", &iValue);

    NonFact(iValue);

    return 0;
}

