#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name   : SumNonFact
//  Description     : It accepts number from user and return summation of all its non factors
//  Input           : Int
//  Output          : Int
//  Author          : Shraddha Dhananjay Mutange
//  Date            : 24/10/2025
// 
////////////////////////////////////////////////////////////////////////////////////////////////

int SumNonFact(int iNo)
{
    int iCnt = 0;
    int iSum = 0;                              // To store multiplication of factors

    for(iCnt = iNo; iCnt >= 1; iCnt--)
    {
        if(((iNo % iCnt) != 0))
        {
            iSum = iSum + iCnt;               // Business logic
        }
    }
    return iSum;
} // End of SumNonFact

////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;                               // To store the result

    printf("Enter number : ");
    scanf("%d", &iValue);

    iRet = SumNonFact(iValue);

    printf("%d", iRet);

    return 0;
}

